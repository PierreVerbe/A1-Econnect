			<h3>Article L111-1</h3>
				<p>" L'auteur d'une oeuvre de l'esprit jouit sur cette oeuvre, du seul fait de sa création, d'un droit de propriété incorporelle exclusif et opposable à tous. Ce droit comporte des attributs d'ordre intellectuel et moral, ainsi que des attributs d'ordre patrimonial " Le code de la propriété intellectuelle définit donc deux composantes au droit d'auteurs</p>

			<h3>Article L112-2</h3>
				<p>Les oeuvres protégées sont :<br />

				- les livres brochures et autres écrits littéraires, artistiques et scientifiques ;<br />

				- les conférences, allocutions, sermons et plaidoiries<br />

				- les oeuvres dramatiques ou dramatico-musicales<br />

				- les oeuvres chorégraphiques, les numéros et tours de cirque, les pantomimes<br />

				- les compositions musicales avec ou sans paroles<br />

				- les oeuvres cinématographiques et autres oeuvres consistant dans des séquences animées d'images, sonorisées ou non, dénommées ensemble " oeuvres audiovisuelles "<br />

				- les oeuvres de dessin, de peinture, d'architecture, de sculpture, de gravure, de lithographie<br />

				- les oeuvres graphiques et typographiques - les oeuvres photographiques et celles réalisées à l'aide de techniques analogues à la photographie<br />

				- les oeuvres des arts appliqués<br />

				- les illustrations, les cartes géographiques<br />

				- les plans, croquis et ouvrages plastiques relatifs à la géographie, à la topographie, à l'architecture et aux sciences<br />

				- les logiciels, y compris le matériel de conception préparatoire<br />

				- les créations des industries saisonnières de l'habillement et de la parure<br />
