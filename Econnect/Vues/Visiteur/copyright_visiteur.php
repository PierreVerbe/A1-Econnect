			<h3>Article L112-3</h3>

				<p>Les auteurs de traduction, d'adaptation transformations ou arrangements des oeuvres de l'esprit jouissent de la même protection , sans préjudice des droits de l'auteur de l'oeuvre originale. Il en est de même des auteurs d'anthologie ou de recueils d'oeuvres qui par le choix et la disposition des matières constituent des créations intellectuelles. Les droits moraux le droit moral vise principalement à permettre à un auteur d'être reconnu en tant que tel. Il est attaché à la personne de l'auteur, inaliénable, imprescriptible, et transmissible aux héritiers. ce droit permet le droit de divulgation de l'oeuvre, de la modifier, d'en définir les conditions d'exploitation, et la possibilité de réunir diverses oeuvres dans un recueil. C'est au titre du droit moral que l'on cite l'auteur d'un document. les droits patrimoniaux les droits patrimoniaux concernent es droits de reproduction et de représentation (en particulier de télédiffusion) Ils appartiennent à l'auteur , qui peut les céder à titre gratuit et onéreux, (il est important d'avoir une trace écrite d'une telle cession)</p>

			<h3>Article L-122-4</h3>

				<p>Toute représentation ou reproduction intégrale ou partielle faite sans le consentement de l'auteur ou de ses ayant droit ou ayant cause est illicite. Il en est de même pour la traduction, l'adaptation ou la transformation, l'arrangement ou la reproduction par un art ou un procédé quelconque.</p>

			<h3>Article 122-5</h3>

				<p>Lorsqu'une oeuvre a été divulgué, l'auteur ne peut interdire - les représentations privées et gratuites effectuées exclusivement dans le cercle de famille - les copies ou reproduction strictement réservées à l'usage privé du copiste et non destinées à une utilisation collective - Sous réserve que soient clairement indiqués le nom de l'auteur et la source` . les analyses et courtes citations justifiées par la caractère critique, polémique, pédagogique, scientifique ou d'information de l'oeuvre à laquelle elles sont incorporées . les revues de presse . la diffusion même intégrale , par voie de presse, à titre d'information d'actualité des discours destinés au public prononcés dans les assemblées politiques administratives, judiciaires ou académiques, ainsi que dans les réunions publiques d'ordre politique et les cérémonies officielles. - la parodie, le pastiche et la caricature, compte tenu des lois du genre...</p>

